public class Ej1 {
    public static void main(String[] args) {
        String nombre = "Ana";
        int edad = 25;
        String ciudad = "Madrid";
        mostrar(nombre, edad, ciudad);
    }

    public static void mostrar(String nombre, int edad, String ciudad) {
        System.out.println(nombre + "\n" + edad + "\n" + ciudad);
    }
}